package cn.kcrxorg.areacashcenter.data.cashBoxRecord;

public class Cash {

    String cashMoney;
    String cashTypeName;
    String cashVoucherName;
    String physicalTypeName;

    public String getCashMoney() {
        return cashMoney;
    }

    public void setCashMoney(String cashMoney) {
        this.cashMoney = cashMoney;
    }

    public String getCashTypeName() {
        return cashTypeName;
    }

    public void setCashTypeName(String cashTypeName) {
        this.cashTypeName = cashTypeName;
    }

    public String getCashVoucherName() {
        return cashVoucherName;
    }

    public void setCashVoucherName(String cashVoucherName) {
        this.cashVoucherName = cashVoucherName;
    }

    public String getPhysicalTypeName() {
        return physicalTypeName;
    }

    public void setPhysicalTypeName(String physicalTypeName) {
        this.physicalTypeName = physicalTypeName;
    }
}
