package cn.kcrxorg.areacashcenter.data.cashBoxRecord;

public class Zk {
    String zkId;
    String count;
    String zkName;
    String zkNumber;

    public String getZkId() {
        return zkId;
    }

    public void setZkId(String zkId) {
        this.zkId = zkId;
    }

    public String getCount() {
        return count;
    }

    public void setCount(String count) {
        this.count = count;
    }

    public String getZkName() {
        return zkName;
    }

    public void setZkName(String zkName) {
        this.zkName = zkName;
    }

    public String getZkNumber() {
        return zkNumber;
    }

    public void setZkNumber(String zkNumber) {
        this.zkNumber = zkNumber;
    }
}
